def run_grant_search(api_key: str) -> dict:
    """
    Three-pass search:
      Pass 1: Broad sweep
      Pass 2: Targeted search for missed opportunities
      Pass 3: Adversarial eligibility audit (NEW in v3)
    """
    client = genai.Client(api_key=api_key)
    today_dt = datetime.now(BRT)
    today = today_dt.strftime("%d/%m/%Y (%A)")

    google_search_tool = types.Tool(
        google_search=types.GoogleSearch()
    )

    config = types.GenerateContentConfig(
        system_instruction=SYSTEM_PROMPT.format(today=today),
        tools=[google_search_tool],
        temperature=0.1,
        max_output_tokens=16000,
        thinking_config=types.ThinkingConfig(
            thinking_level="HIGH"
        ),
    )

    # --- PASS 1: Broad sweep ---
    logger.info(f"PASS 1: Broad sweep — model={GEMINI_MODEL}, date={today}")
    user_prompt = USER_PROMPT_TEMPLATE.format(today=today)

    all_opportunities = []
    all_recommendations = []

    try:
        response = client.models.generate_content(
            model=GEMINI_MODEL,
            contents=user_prompt,
            config=config,
        )

        _log_grounding_metadata(response)
        data = extract_json_from_response(response.text)

        if data and "opportunities" in data:
            all_opportunities.extend(data["opportunities"])
            logger.info(f"Pass 1 found {len(data['opportunities'])} opportunities")
            if "strategic_recommendations" in data:
                all_recommendations = data["strategic_recommendations"]
        else:
            logger.warning("Pass 1: Could not parse structured data. Using raw text.")
            return {"raw_text": response.text, "parse_failed": True}

    except Exception as e:
        logger.error(f"Gemini API error (Pass 1): {e}")
        sys.exit(1)

    # --- PASS 2: Targeted search for missed opportunities ---
    logger.info("PASS 2: Targeted search for missed opportunities")

    found_names = "\n".join(
        f"- {opp.get('name', 'Unknown')}" for opp in all_opportunities
    )

    second_prompt = SECOND_PASS_PROMPT.format(
        today=today,
        found_names=found_names,
    )

    try:
        response2 = client.models.generate_content(
            model=GEMINI_MODEL,
            contents=second_prompt,
            config=config,
        )

        _log_grounding_metadata(response2)
        data2 = extract_json_from_response(response2.text)

        if data2 and "opportunities" in data2:
            all_opportunities.extend(data2["opportunities"])
            logger.info(f"Pass 2 found {len(data2['opportunities'])} additional opportunities")
            if "strategic_recommendations" in data2 and not all_recommendations:
                all_recommendations = data2["strategic_recommendations"]

    except Exception as e:
        logger.warning(f"Pass 2 failed (non-fatal): {e}")

    # --- Post-processing before audit ---
    all_opportunities = [validate_opportunity(opp) for opp in all_opportunities]
    all_opportunities = deduplicate_opportunities(all_opportunities)
    all_opportunities = [
        opp for opp in all_opportunities
        if classify_urgency(opp, today_dt) != "⚫"
    ]

    # =========================================================================
    # PASS 3: Adversarial Eligibility Audit (NEW in v3)
    # =========================================================================
    # This pass acts as a "skeptic auditor" that tries to DISPROVE eligibility.
    # It receives the full list and checks each one against official sources.
    logger.info("PASS 3: Adversarial eligibility audit")

    # Build the list of opportunities to audit
    audit_list = "\n".join(
        f"{i+1}. {opp.get('name', 'Unknown')} — Financiador: {opp.get('funder', 'Unknown')} — "
        f"URL: {opp.get('url', 'N/A')} — "
        f"Elegibilidade declarada: {opp.get('eligibility', 'N/A')} — "
        f"Regiões declaradas: {', '.join(opp.get('eligible_regions', ['N/A']))}"
        for i, opp in enumerate(all_opportunities)
    )

    audit_prompt = ELIGIBILITY_AUDIT_PROMPT.format(
        today=today,
        opportunities_to_audit=audit_list,
    )

    # Audit uses a separate config (no system instruction, just the audit prompt)
    audit_config = types.GenerateContentConfig(
        tools=[google_search_tool],
        temperature=0.0,  # Maximum factuality for audit
        max_output_tokens=12000,
        thinking_config=types.ThinkingConfig(
            thinking_level="HIGH"
        ),
    )

    try:
        response3 = client.models.generate_content(
            model=GEMINI_MODEL,
            contents=audit_prompt,
            config=audit_config,
        )

        _log_grounding_metadata(response3)
        audit_data = extract_json_from_response(response3.text)

        if audit_data and "audit_results" in audit_data:
            audit_results = audit_data["audit_results"]
            logger.info(f"Pass 3 audited {len(audit_results)} opportunities")

            # Apply audit results: may override eligibility
            all_opportunities = apply_audit_results(all_opportunities, audit_results)

            # Count disqualifications
            disqualified = sum(
                1 for ar in audit_results if ar.get("brazil_eligible") is False
            )
            if disqualified > 0:
                logger.info(f"Pass 3 disqualified {disqualified} opportunities")
        else:
            logger.warning("Pass 3: Could not parse audit results. Continuing without audit.")

    except Exception as e:
        logger.warning(f"Pass 3 (audit) failed (non-fatal): {e}")

    # --- Final post-processing ---
    all_opportunities = validate_links(all_opportunities)

    for opp in all_opportunities:
        opp["urgency"] = classify_urgency(opp, today_dt)

    all_opportunities = sort_opportunities(all_opportunities, today_dt)

    # Split into confirmed and unverified
    confirmed, unverified = filter_by_eligibility(all_opportunities)

    logger.info(
        f"Final: {len(confirmed)} confirmed + {len(unverified)} unverified = "
        f"{len(confirmed) + len(unverified)} total"
    )

    return {
        "opportunities_confirmed": confirmed,
        "opportunities_unverified": unverified,
        "opportunities": confirmed + unverified,  # Combined for backward compat
        "strategic_recommendations": all_recommendations,
        "generated_at": today_dt.isoformat(),
        "model": GEMINI_MODEL,
        "stats": {
            "pass1_count": len(all_opportunities),  # Before filtering
            "confirmed_count": len(confirmed),
            "unverified_count": len(unverified),
            "removed_count": len(all_opportunities) - len(confirmed) - len(unverified),
        },
    }


def _log_grounding_metadata(response) -> None:
    """Log search queries and grounding info from the API response."""
    try:
        if response.candidates and response.candidates[0].grounding_metadata:
            metadata = response.candidates[0].grounding_metadata
            if metadata.web_search_queries:
                logger.info(f"Search queries: {metadata.web_search_queries}")
            if metadata.grounding_chunks:
                logger.info(f"Grounding sources: {len(metadata.grounding_chunks)} chunks")
    except Exception:
        pass


