def filter_by_eligibility(opps: list[dict]) -> tuple[list[dict], list[dict]]:
    """
    Split opportunities into confirmed and unverified based on eligibility.

    Returns:
        Tuple of (confirmed_list, unverified_list)
        - confirmed_list: brazil_eligible is True or confidence is confirmed/likely
        - unverified_list: brazil_eligible is None or confidence is unverified/partial
    """
    confirmed = []
    unverified = []

    for opp in opps:
        # Explicitly ineligible → REMOVE completely (don't even show)
        if opp.get("brazil_eligible") is False:
            logger.info(
                f"Eligibility filter: REMOVED '{opp.get('name')}' "
                f"(brazil_eligible=false, reason: {opp.get('eligibility', 'N/A')})"
            )
            continue

        confidence = opp.get("eligibility_confidence", "unverified")

        if confidence in ("confirmed", "likely") and opp.get("brazil_eligible") is True:
            confirmed.append(opp)
        elif confidence == "partial":
            # Partial eligibility (e.g., partner only) → show in unverified with note
            unverified.append(opp)
        else:
            # unverified, or brazil_eligible is None
            unverified.append(opp)

    logger.info(
        f"Eligibility filter: {len(confirmed)} confirmed, "
        f"{len(unverified)} unverified, "
        f"{len(opps) - len(confirmed) - len(unverified)} removed"
    )

    return confirmed, unverified


def apply_audit_results(opps: list[dict], audit_results: list[dict]) -> list[dict]:
    """
    Merge Pass 3 audit results back into the opportunity list.

    The audit may downgrade eligibility_confidence or set brazil_eligible=false.
    """
    # Build lookup by normalized name
    audit_map = {}
    for ar in audit_results:
        key = re.sub(r'[^a-z0-9]', '', ar.get("name", "").lower())
        audit_map[key] = ar

    updated = []
    for opp in opps:
        key = re.sub(r'[^a-z0-9]', '', opp.get("name", "").lower())

        if key in audit_map:
            ar = audit_map[key]

            # Audit says NOT eligible → override
            if ar.get("brazil_eligible") is False:
                opp["brazil_eligible"] = False
                opp["eligibility_confidence"] = "confirmed"
                opp["eligibility"] += (
                    f" — ❌ AUDITORIA: {ar.get('disqualification_reason', 'Brasil não elegível')}"
                )
                logger.info(
                    f"Audit override: '{opp.get('name')}' marked as INELIGIBLE — "
                    f"{ar.get('disqualification_reason', 'no reason')}"
                )

            # Audit confirms eligibility → upgrade confidence
            elif ar.get("brazil_eligible") is True:
                if ar.get("eligibility_confidence") == "confirmed":
                    opp["eligibility_confidence"] = "confirmed"
                    opp["brazil_eligible"] = True
                    if ar.get("audit_notes"):
                        opp["eligibility_source"] = ar.get("audit_notes", "")

            # Audit found regions info → update
            if ar.get("eligible_regions_found"):
                opp["eligible_regions"] = ar["eligible_regions_found"]

        updated.append(opp)

    return updated


