def extract_json_from_response(text: str) -> Optional[dict]:
    """Extract and parse JSON from LLM response, handling markdown fences."""
    text = text.strip()

    if text.startswith("```"):
        text = re.sub(r"^```(?:json)?\s*\n?", "", text)
        text = re.sub(r"\n?```\s*$", "", text)
        text = text.strip()

    try:
        return json.loads(text)
    except json.JSONDecodeError:
        pass

    match = re.search(r'\{[\s\S]*\}', text)
    if match:
        try:
            return json.loads(match.group())
        except json.JSONDecodeError:
            pass

    logger.warning("Could not parse JSON from response. Falling back to raw text.")
    return None


def validate_opportunity(opp: dict) -> dict:
    """Validate and normalize a single opportunity entry, including eligibility fields."""
    # Required fields
    required_fields = ["name", "funder", "summary", "url"]
    for field in required_fields:
        if field not in opp or not opp[field]:
            opp[field] = "⚠️ Não informado"

    # Normalize deadline
    if "deadline" not in opp:
        opp["deadline"] = "rolling"
        opp["deadline_type"] = "rolling"
        opp["deadline_display"] = "Fluxo contínuo"

    if "deadline_type" not in opp:
        opp["deadline_type"] = "fixed" if opp.get("deadline", "").count("-") == 2 else "rolling"

    if "deadline_display" not in opp:
        opp["deadline_display"] = opp.get("deadline", "Verificar no site")

    # Amount fields
    if "amount_display" not in opp or not opp["amount_display"]:
        opp["amount_display"] = "Não divulgado"

    for field in ["amount_usd_min", "amount_usd_max"]:
        if field not in opp:
            opp[field] = 0

    if "themes" not in opp:
        opp["themes"] = []

    if "type" not in opp:
        opp["type"] = "grant"

    if "eligibility" not in opp:
        opp["eligibility"] = "Verificar no site"

    # ---------------------------------------------------------------------------
    # NEW IN v3: Eligibility verification fields
    # ---------------------------------------------------------------------------
    # eligibility_confidence: How sure are we about the eligibility?
    #   "confirmed" — verified on official page
    #   "likely" — appears global but no explicit country list
    #   "partial" — eligible as partner only (not lead)
    #   "unverified" — could not confirm
    if "eligibility_confidence" not in opp:
        opp["eligibility_confidence"] = "unverified"

    # eligibility_source: URL where eligibility was checked
    if "eligibility_source" not in opp:
        opp["eligibility_source"] = ""

    # eligible_regions: List of regions/countries from official source
    if "eligible_regions" not in opp:
        opp["eligible_regions"] = []

    # brazil_eligible: Explicit boolean — is Brazil eligible?
    # null/None means "not confirmed either way" → treated as unverified
    if "brazil_eligible" not in opp:
        opp["brazil_eligible"] = None

    return opp


def classify_urgency(opp: dict, today: datetime) -> str:
    """Classify opportunity urgency based on deadline proximity."""
    if opp.get("deadline_type") == "rolling":
        return "🟢"

    deadline_str = opp.get("deadline", "")
    if not deadline_str or deadline_str == "rolling" or "verificar" in deadline_str.lower():
        return "🟢"

    try:
        deadline_date = datetime.strptime(deadline_str, "%Y-%m-%d").replace(tzinfo=BRT)
        days_until = (deadline_date - today).days

        if days_until < 0:
            return "⚫"
        elif days_until <= 30:
            return "🔴"
        elif days_until <= 90:
            return "🟡"
        else:
            return "🟢"
    except ValueError:
        return "🟢"


